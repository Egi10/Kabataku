package id.buaja.kabataku;

public class Hasil {
    private String angkaPertama;
    private String angkaKedua;
    private String angkaHasil;
    private String image;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getAngkaPertama() {
        return angkaPertama;
    }

    public void setAngkaPertama(String angkaPertama) {
        this.angkaPertama = angkaPertama;
    }

    public String getAngkaKedua() {
        return angkaKedua;
    }

    public void setAngkaKedua(String angkaKedua) {
        this.angkaKedua = angkaKedua;
    }

    public String getAngkaHasil() {
        return angkaHasil;
    }

    public void setAngkaHasil(String angkaHasil) {
        this.angkaHasil = angkaHasil;
    }
}
